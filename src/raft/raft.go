//
// the service or tester wants to create a Raft server. the ports
// of all the Raft servers (including this one) are in peers[]. this
// server's port is peers[me]. all the servers' peers[] arrays
// have the same order. persister is a place for this server to
// save its persistent state, and also initially holds the most
// recent saved state, if any. applyCh is a channel on which the
// tester or service expects Raft to send ApplyMsg messages.
// Make() must return quickly, so it should start goroutines
// for any long-running work.
//
func Make(peers []*labrpc.ClientEnd, me int,
	persister *Persister, applyCh chan ApplyMsg) *Raft {
	rf := &Raft{}
	rf.peers = peers
	rf.persister = persister
	rf.me = me

	// Your initialization code here (2A, 2B, 2C).
	rf.role = Follower
	rf.currentTerm = 0
	rf.lastHearBeat = time.Now().UnixNano() / 1e6
	rf.voteFor = -1
	//leader定时发送心跳
	go func() {
		rf.startHeartbeat()
	}()
	//心跳超时检测
	go func() {
		for true {
			rf.heartbeatTimeout()
		}
	}()

	// initialize from state persisted before a crash
	rf.readPersist(persister.ReadRaftState())

	// start ticker goroutine to start elections
	go rf.ticker()

	return rf
}

func (rf *Raft) startHeartbeat() {
	for true {
		if rf.role == Leader {
			voteCnt := rf.sendHeartbeat()
			if voteCnt <= len(rf.peers)/2 {
				//变为候选人，等待下一次选举
				rf.role = Candidate
			}
		}
		time.Sleep(HeartbeatInterval * time.Millisecond)
	}
}

func (rf *Raft) heartbeatTimeout() {
	for true {
		now := time.Now().UnixNano() / 1e6
		if now-rf.lastHearBeat > HeartbeatTimeout {
			//fmt.Printf("heartbeat timeout...%d\n", rf.me)
			switch rf.role {
			case Leader:
				rf.role = Candidate
			case Follower:
				rf.role = Candidate
			case Candidate:
				rf.currentTerm++
				//发送选举给其他服务器
				voteCnt := rf.sendHeartbeat()
				if voteCnt > len(rf.peers)/2 {
					rf.role = Leader
					fmt.Printf("become leader...%d\n", rf.me)
				}
			}
		}
		time.Sleep(HeartbeatTimeout * time.Millisecond)
	}
}

func (rf *Raft) sendHeartbeat() int {
	//fmt.Printf("send heartbeat ...id:%d\n", rf.me)
	var wg sync.WaitGroup
	voteCnt := 0
	for i := 0; i < len(rf.peers); i++ {
		wg.Add(1)
		go func(i int) {
			defer wg.Done()
			args := RequestVoteArgs{Term: rf.currentTerm, CandidateId: rf.me}
			reply := RequestVoteReply{}
			if rf.sendRequestVote(i, &args, &reply) && reply.VoteGrant {
				voteCnt++
			}
		}(i)
	}
	wg.Wait()
	return voteCnt
}
